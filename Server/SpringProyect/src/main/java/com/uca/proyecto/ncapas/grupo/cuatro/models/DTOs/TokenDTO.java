package com.uca.proyecto.ncapas.grupo.cuatro.models.DTOs;

public class TokenDTO {
	
	private String token;

	public TokenDTO(String token) {
		super();
		this.token = token;
	}

	public TokenDTO() {
		super();
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}
	
	
}
